// Regenerates src/lib/zine/theme/palettes.data.ts from the nice-color-palettes dataset.
//
// Why a committed module instead of importing the package JSON directly: a node_modules JSON
// import (static or dynamic, main or subpath) resolves under Vite dev/vitest but is dropped
// by the production Rollup build with no warning — leaving the theme catalogue empty for real
// users. Emitting the data as a first-party `.ts` const guarantees it bundles, and keeps it
// editor-only (only catalogue.ts imports it). Run: `pnpm build:themes`.
import { createRequire } from 'node:module';
import { writeFileSync } from 'node:fs';

const require = createRequire(import.meta.url);
const palettes = require('nice-color-palettes/200.json');

const rows = palettes
	.filter((p) => Array.isArray(p) && p.length >= 3)
	.map((p) => '\t' + JSON.stringify(p))
	.join(',\n');

const out = `// AUTO-GENERATED by scripts/build-theme-catalogue.mjs — do not edit by hand.
// Source: nice-color-palettes (the 200 most-loved ColourLovers palettes). Committed as a
// first-party module so the dataset bundles reliably (a node_modules JSON import is dropped
// by the production build) and stays editor-only. Regenerate with \`pnpm build:themes\`.
export const NICE_PALETTES: string[][] = [
${rows}
];
`;

writeFileSync(new URL('../src/lib/zine/theme/palettes.data.ts', import.meta.url), out);
console.log(`wrote ${palettes.length} palettes to src/lib/zine/theme/palettes.data.ts`);
